/// Decrypt functions operating on PGM68KROM (the main 68K program ROM).
///
/// All functions read/write the global `PGM68KROM` array whose length is
/// given by `nPGM68KROMLen` (both declared in C++/pgm_run.cpp).
///
/// On little-endian targets (the only targets currently supported)
/// `BURN_ENDIAN_SWAP_INT16` is a no-op, so no byte-swap is performed here.

use core::slice;

use super::ffi::{PGM68KROM, nPGM68KROMLen};

// ============================================================================
// Helper – obtain a mutable slice over the 68K ROM
// ============================================================================

/// # Safety
/// Caller must ensure the C-side globals are valid and non-null.
#[inline]
unsafe fn rom_words() -> &'static mut [u16] {
    let ptr = unsafe { PGM68KROM } as *mut u16;
    let len = (unsafe { nPGM68KROMLen } / 2) as usize;
    unsafe { slice::from_raw_parts_mut(ptr, len) }
}

// ============================================================================
// Dragon World 2
// ============================================================================

#[unsafe(no_mangle)]
pub extern "C" fn pgm_decrypt_dw2() {
    let words = unsafe { rom_words() };
    for (i, w) in words.iter_mut().enumerate() {
        let mut x = *w;
        if (i & 0x020890) == 0x000000 { x ^= 0x0002; }
        if (i & 0x020000) == 0x020000 && (i & 0x001500) != 0x001400 { x ^= 0x0002; }
        if (i & 0x020400) == 0x000000 && (i & 0x002010) != 0x002010 { x ^= 0x0400; }
        if (i & 0x020000) == 0x020000 && (i & 0x000148) != 0x000140 { x ^= 0x0400; }
        *w = x;
    }
}

