/// Decrypt functions operating on PGMUSER0 (the external ARM user ROM).
///
/// All functions read/write the global `PGMUSER0` array whose length is
/// given by `nPGMExternalARMLen` (both declared in C++/pgm_run.cpp).

use core::slice;

use super::ffi::{PGMUSER0, nPGMExternalARMLen};

// ============================================================================
// Helper – obtain a mutable slice over the ARM ROM
// ============================================================================

/// # Safety
/// Caller must ensure the C-side globals are valid and non-null.
#[inline]
unsafe fn arm_words() -> &'static mut [u16] {
    let ptr = unsafe { PGMUSER0 } as *mut u16;
    let len = (unsafe { nPGMExternalARMLen } / 2) as usize;
    unsafe { slice::from_raw_parts_mut(ptr, len) }
}

// ============================================================================
// SVG (Spectral Vs Generation)
// ============================================================================

#[unsafe(no_mangle)]
pub extern "C" fn pgm_decrypt_svg() {
    let words = unsafe { arm_words() };
    for (i, w) in words.iter_mut().enumerate() {
        let mut x = *w;
        if (i & 0x040080) != 0x000080 { x ^= 0x0001; }
        if (i & 0x004008) == 0x004008 { x ^= 0x0002; }
        if (i & 0x080030) == 0x080010 { x ^= 0x0004; }
        if (i & 0x000042) != 0x000042 { x ^= 0x0008; }
        if (i & 0x048100) == 0x048000 { x ^= 0x0010; }
        if (i & 0x002004) != 0x000004 { x ^= 0x0020; }
        if (i & 0x011800) != 0x010000 { x ^= 0x0040; }
        if (i & 0x000820) == 0x000820 { x ^= 0x0080; }
        // no XOR table
        *w = x;
    }
}

